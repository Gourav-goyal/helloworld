puts("add operator")   #Adds values from both sides of the operator.
puts(10 + 20)     
 
puts("subtract operator")   #Subtract values from both sides of the operator.
puts(35 - 15)    

puts("multiply operator")   #Multiply values from both sides of the operator.
puts(4 * 8)   

puts("division operator")   #Divide left side operand with right side operand.
puts(25 / 5)   

puts("exponential operator")   #Right side operand becomes the exponent of left side operand.
puts(5 ** 2)   

puts("modulo operator")   #Divide left side operand with right side operand returning remainder.
puts(25 % 4)   

puts("Ternary operator")  #Ternary operators first check whether given conditions are true or false 
puts(2<5 ? 5:2)   
puts(5<2 ? 5:2)   

